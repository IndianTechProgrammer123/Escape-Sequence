# Escape Sequence In Python
# ______________________________________________________________
# |__Escape Sequence___|____Description____|____Output__________|
# |       \'           |   Single quote    |     '              |
# |       \"           |   Double quote    |     "              |
# |       \\           |   Back slash      |     \              |
# |       \t           |   Tab             |     hello   abc    |
# |       \n           |   New Line        |     hello          |
# |____________________|___________________|_____123____________|

print('hello\'')
print('hello\"')
print('hello\\')
print('hello\tabc')
print('hello\n123') 